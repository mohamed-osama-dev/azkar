  List<AzkarModel> getAllAzkar() {
    final data = getAzkarData();
    return data.asMap().entries.map((entry) {
      return AzkarModel.fromJson(entry.value, entry.key);
    }).toList();
  }

  AzkarModel? getAzkarById(int id) {
    final data = getAzkarData();
    if (id >= 0 && id < data.length) {
      return AzkarModel.fromJson(data[id], id);
    }
    return null;
  }
